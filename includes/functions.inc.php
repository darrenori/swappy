function setCookieSameSite(string $name, string $value, $expire)
{


    $domain = 'www.swapamc.com';
    $samesite = 'Strict';
    $secure = true;
    $httponly = true;
    $path = '/';


    if (PHP_VERSION_ID < 70300) {
        setcookie($name, $value, $expire, $path . '; samesite=' . $samesite, $domain, $secure, $httponly);
        return;
    } else
        setcookie($name, $value, [
            'expires' => $expire,
            'path' => $path,
            'domain' => $domain,
            'samesite' => $samesite,
            'secure' => $secure,
            'httponly' => $httponly,
        ]);
}
